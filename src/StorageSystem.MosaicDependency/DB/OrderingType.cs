
namespace CareFusion.Mosaic.DB
{
    /// <summary>
    /// Defines the different supported types of a query ordering.
    /// </summary>
    public enum OrderingType
    {
        /// <summary>
        /// Identifies an regular ascending ordering (small to large).
        /// </summary>
        Asc,

        /// <summary>
        /// Identifies an regular descending ordering (large to small).
        /// </summary>
        Desc
    }
}
